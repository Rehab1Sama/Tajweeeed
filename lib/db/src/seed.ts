async function main() {
  console.log("🌱 Seeding نور التجويد database...\n");

  // 1. Courses
  console.log("📚 Inserting courses...");
  const insertedCourses = await db
    .insert(coursesTable)
    .values(courses)
    .onConflictDoNothing()
    .returning();
  console.log(`   ✓ ${insertedCourses.length} courses inserted`);

  // 2. Tajweed Rules
  console.log("📖 Inserting tajweed rules...");
  const insertedRules = await db
    .insert(tajweedRulesTable)
    .values(tajweedRules)
    .onConflictDoNothing()
    .returning();
  console.log(`   ✓ ${insertedRules.length} rules inserted`);

  if (insertedRules.length === 0) {
    console.log("   ℹ  Rules already seeded — fetching existing IDs...");
    const existing = await db.select({ id: tajweedRulesTable.id }).from(tajweedRulesTable);
    insertedRules.push(...(existing as typeof insertedRules));
  }

  // 3. Lessons
  console.log("📝 Inserting lessons...");
  const ruleIds = insertedRules.map((r) => r.id);
  const lessonsWithRuleIds = lessonsData.map((l) => ({
    title: l.title,
    content: l.content,
    level: l.level,
    orderIndex: l.orderIndex,
    ruleId: l._ruleIdx !== null ? (ruleIds[l._ruleIdx] ?? null) : null,
    videoUrl: l.videoUrl,
  }));

  const insertedLessons = await db
    .insert(lessonsTable)
    .values(lessonsWithRuleIds)
    .onConflictDoNothing()
    .returning();
  console.log(`   ✓ ${insertedLessons.length} lessons inserted`);

  // 4. Daily Wird
  console.log("🌙 Inserting daily wird...");
  const wirdValues = dailyWirdData.map((w) => ({
    date: dateStr(w.daysOffset),
    ruleId: ruleIds[w.ruleIdx] ?? null,
    title: w.title,
    content: w.content,
  }));

  const insertedWird = await db
    .insert(dailyWirdTable)
    .values(wirdValues)
    .onConflictDoNothing()
    .returning();
  console.log(`   ✓ ${insertedWird.length} wird entries inserted`);

  // 5. Comment Templates
  console.log("💬 Inserting comment templates...");
  const insertedTemplates = await db
    .insert(commentTemplatesTable)
    .values(commentTemplatesData)
    .onConflictDoNothing()
    .returning();
  console.log(`   ✓ ${insertedTemplates.length} comment templates inserted`);

  console.log("\n✅ Seed complete!");
  process.exit(0);
}

main().catch((err) => {
  console.error("❌ Seed failed:", err);
  process.exit(1);
});
